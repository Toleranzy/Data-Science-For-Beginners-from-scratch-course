# 4) Что такое PEP 8?
#
# PEP 8 — это рекомендации по стилю написания Python-кода.
#
# В них указано, как называть переменные, делать отступы, переносить длинные строки и оформлять код, чтобы он был понятным и одинаковым у разных программистов.
#
# ```text
# https://peps.python.org/pep-0008/
# ```
#
# 4.1) Линтеры проверяют PEP 8?
#
# Да.
#
# Например, `Flake8` проверяет многие правила PEP 8: отступы, длину строк, пробелы и другие ошибки стиля.
#
# `Pylint` тоже может проверять стиль кода, но у него есть и другие проверки.
#
# 4.2) Какая нотация используется для создания переменных?
#
# Для переменных в Python используется `snake_case`.
#
# 4.3) Может ли переменная состоять из одной буквы, например underscore `_`?
#
# Да.
#
# 4.4) Зачем и где мы используем underscore `_`?
#
# Underscore `_` используют в названиях переменных и функций, чтобы разделять слова в стиле `snake_case`.
#
# Также `_` используют как переменную, значение которой не нужно, например в цикле:
#
# ```python
# for _ in range(5):
#     print("Привет")
# ```
#
# 4.5) По PEP 8 допустима переменная в одну букву?
#
# Да, допустима, например `i`, `j` или `x` для коротких локальных переменных и счётчиков в цикле.
#
# Но PEP 8 не рекомендует использовать `l`, `O` и `I`, потому что их легко перепутать с цифрами.

