# 3) Почему расширение py лучше, чем ipynb?
#
# Файл `.py` удобнее для Git и проверки кода линтерами. В нём хранится только обычный Python-код и текстовые комментарии, поэтому изменения в коммитах легче читать и проверять.
#
# Файл `.ipynb` внутри хранится как большой JSON-файл: там есть ячейки, результаты запусков и другая служебная информация. Из-за этого при сравнении версий могут появляться лишние изменения и конфликты.
