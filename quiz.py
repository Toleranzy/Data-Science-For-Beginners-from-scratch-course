# 24) Где посмотреть что есть конфликт в файлах?
# Конфликт можно увидеть в VS Code или Cursor: файл появится в разделе Changes как конфликтный, а внутри файла будут кнопки `Accept Current Change`, `Accept Incoming Change` и `Accept Both Changes`.
# 24.1) Когда он появляется?
# Конфликт появляется, когда в одном и том же месте одного файла были сделаны разные изменения: например, ты изменил файл локально, а коллега изменил те же строки в GitHub. Обычно это может случиться после `pull`, `merge` или восстановления stash.
# 25) Как решить конфликт в файлах?
# В VSC откроем проблемный файл. зелененький текст это с гитхаба. синий это локальные изменения. Accept Current Change принять текущие изменения. Accept Incoming Change принять из вне изменения. Accept Both Changes принять оба. Либо вручную в файле удалить что ненужно и потом нажатьResolve in Merge Editor. Потом complete merge.
