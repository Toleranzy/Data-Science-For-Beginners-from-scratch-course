# 24) Где посмотреть, что есть конфликт в файлах?
#
# Конфликт можно увидеть в VS Code или Cursor: файл появится в разделе Changes как конфликтный, а внутри файла будут кнопки:
#
# - `Accept Current Change`
# - `Accept Incoming Change`
# - `Accept Both Changes`
#
# 24.1) Когда он появляется?
#
# Конфликт появляется, когда в одном и том же месте одного файла были сделаны разные изменения: например, ты изменил файл локально, а коллега изменил те же строки в GitHub.
#
# Обычно это может случиться после `pull`, `merge` или восстановления stash.
#
