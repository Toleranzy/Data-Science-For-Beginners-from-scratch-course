# 23.3) Различие между stash и коммитом. Когда лучше сохранить изменения в stash, а когда коммитить?
#
# Коммит — это сохранение законченных изменений в историю Git. Его можно потом отправить на GitHub через `push`.
#
# Stash — это временный буфер для незакоммиченных изменений. Он нужен, например, когда коллега изменил тот же файл и нужно сначала сделать `pull`, а свои текущие изменения временно спрятать.
#
# В stash лучше сохранять изменения, когда работа ещё не закончена, коммит делать рано или нужно быстро переключиться на другую ветку / принять `pull`.
#
# Коммит лучше делать, когда часть работы закончена, изменения понятны и их можно сохранить в историю и отправить на GitHub.
#
