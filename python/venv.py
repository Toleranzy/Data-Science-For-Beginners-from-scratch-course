# 8.Где по умолчанию больше пакетов venv/pip или conda? и почему дата сайнинисты используют conda?
#
# #По умолчанию больше пакетов доступно через pip, потому что pip устанавливает пакеты из PyPI — самого большого репозитория Python-библиотек. Там обычно быстрее появляются новые версии и больше небольших библиотек.
#
# #Conda часто используют дата-сайентисты, потому что она удобнее для научных и ML-проектов. Conda умеет устанавливать не только Python-пакеты, но и системные зависимости: например библиотеки для NumPy, pandas, scipy, OpenCV и другие бинарные компоненты. Это снижает риск ошибок при установке сложных пакетов.
#
# #Еще conda хорошо управляет окружениями: можно создать отдельное окружение под проект, указать нужную версию Python и зависимости, а потом перенести это окружение через environment.yml.
