"""Issues."""

# Общие вопросы
#
# Что такое Issues на GitHub и для чего они используются?
#
# # Это проблемы которые надо отслеживать и решить.
#
# Чем Issues отличаются от других инструментов управления задачами?
#
# # Issues отличаются тем, что они интегрированы с GitHub и позволяют связывать задачи с кодом, коммитами и Pull Request.
#
# Какие основные компоненты (поля) есть у каждого Issue?
#
# # Основные поля Issue: название (Title), описание (Description), исполнитель (Assignee), метки (Labels), проект (Projects), веха (Milestone), комментарии и вложения. Эти поля помогают описать задачу, назначить ответственных и отслеживать её выполнение.
#
# Создание Issues
#
# Как создать новое Issue в репозитории?
#
# # Открыть нужный репозиторий.
# # Перейти во вкладку Issues.
# # Нажать кнопку New issue.
# # Указать название задачи (Title).
# # Добавить описание проблемы или задачи (Description).
# # При необходимости назначить исполнителя (Assignees), добавить метки (Labels), проект или другие параметры.
# # Нажать Submit new issue для создания.
#
# Какие данные рекомендуется указывать в описании Issue для лучшего понимания задачи?
#
# # в описании Issue нужно указывать ссылку на связанную задачу и её номер.
#
# Какие теги (labels) можно добавить к Issue? Какие из них стандартные?
#
# # Стандартные Labels GitHub: bug, documentation, duplicate, enhancement, good first issue, help wanted, invalid, question, wontfix. Они используются для управления задачей.
#
# Как прикрепить Assignees (ответственных) к Issue?
#
# # Открыть нужный Issue в репозитории GitHub.
# # В правой части страницы найти раздел Assignees.
# # Нажать Assign yourself (назначить себя) или выбрать нужного пользователя из списка.
# # После выбора пользователь будет указан как ответственный за выполнение задачи.
#
# Работа с Issues
#
# Как использовать Labels для классификации задач?
#
# # Labels помогают классифицировать задачи: ошибки (bug), новые функции (feature), улучшения (enhancement), документацию и приоритеты. Их добавляют через раздел Labels в Issue.
#
# Для чего нужен Milestone, и как связать его с Issue?
#
# # Milestone (веха) нужен для объединения нескольких Issues и Pull Request в одну большую цель или этап разработки. Он помогает отслеживать прогресс выполнения задач, например подготовку нового релиза или версии проекта.
#
# # Чтобы связать Milestone с Issue:
#
# # Открыть нужный Issue.
# # В правой панели найти раздел Milestone.
# # Выбрать существующий Milestone или создать новый.
# # После выбора Issue будет привязан к этой вехе.
#
# Как привязать Issue к пул-реквесту (Pull Request)?
#
# # Указать номер Issue в описании Pull Request Closes (#15).
#
# Как добавить комментарий к существующему Issue?
#
# # Открыть нужный Issue в репозитории.
# # Прокрутить страницу до блока Comments.
# # Ввести текст комментария.
# # Нажать Comment или Add comment.
#
# Закрытие и завершение Issues
#
# Как закрыть Issue вручную?
#
# # Открыть нужный Issue.
# # Внизу страницы или справа найти кнопку Close issue.
# # Нажать её.
# # При необходимости добавить комментарий с причиной закрытия.
#
# # После этого Issue перейдёт в статус Closed.
#
# Можно ли автоматически закрыть Issue с помощью сообщения в коммите или пул-реквесте? Как это сделать?
#
# # Да. В сообщении коммита или Pull Request нужно написать (Closes (#15), Fixes (#15), Resolves (#15)). После merge Issue закроется автоматически.
#
# Как повторно открыть закрытое Issue, если работа ещё не завершена?
#
# # Открыть нужный закрытый Issue в репозитории.
# # Внизу страницы нажать кнопку Reopen issue.
# # После этого Issue снова перейдёт в статус Open и с ним можно продолжить работу.
#
# Фильтрация и поиск
#
# Как найти все открытые или закрытые Issues в репозитории?
#
# # Открыть репозиторий на GitHub.
# # Перейти во вкладку Issues.
# # Использовать фильтры:
# # Open показать открытые Issues;
# # Closed показать закрытые Issues.
#
# Как использовать фильтры для поиска Issues по меткам, исполнителям или другим критериям?
#
# # Открыть вкладку Issues в репозитории.
# # Использовать строку поиска и фильтры:
# # Label поиск задач по меткам (bug, feature, documentation и т.д.);
# # Assignee поиск задач по ответственному пользователю;
# # Author поиск задач по автору;
# # Milestone поиск задач, связанных с определённой вехой;
# # Status фильтр открытых (Open) или закрытых (Closed) Issues.
#
# Как сортировать Issues по приоритету, дате создания или другим параметрам?
#
# # Issues можно сортировать через меню Sort по дате создания, обновления, количеству комментариев и другим параметрам. Приоритет обычно задают через Labels или GitHub Projects.
#
# Интеграции и автоматизация
#
# Как настроить автоматические уведомления о новых или изменённых Issues?
#
# # Нажать Subscribe/Watch в Issue или репозитории и выбрать уровень уведомлений. Дополнительно настроить уведомления в GitHub Settings потом Notifications.
#
# Что такое Projects в контексте GitHub, и как связать их с Issues?
#
# # Projects это инструмент GitHub для управления задачами и отслеживания прогресса. Issue можно добавить в Project через поле Projects в правой панели Issue.
#
# Какие сторонние инструменты можно использовать для автоматизации работы с Issues (например, боты, Webhooks)?
#
# # Для автоматизации Issues используют GitHub Actions, Webhooks и ботов, которые могут автоматически создавать комментарии, назначать исполнителей, добавлять метки
#
# Коллаборация
#
# Как упомянуть другого пользователя в комментарии к Issue?
#
# # В комментарии написать @username (например, @Toleranzy) — пользователь будет упомянут и получит уведомление.
#
# Как запросить дополнительные данные или уточнения у автора Issue?
#
# # Добавить комментарий к Issue с вопросом или просьбой предоставить дополнительные данные (@username для уведомления автора).
#
# Что делать, если Issue неактуально или его нужно объединить с другим?
#
# # Неактуальное Issue нужно закрыть (Close issue). Если оно дубликат — указать ссылку на основное Issue, добавить метку duplicate и закрыть повторное.
#
# Практические аспекты
#
# Как использовать шаблоны для создания Issues?
#
# # Issue Templates позволяют создавать Issues по заранее заданной форме. Шаблоны хранятся в .github/ISSUE_TEMPLATE/ и автоматически предлагаются при создании новой задачи.
#
# Что такое Linked Issues, и как создать связь между задачами?
#
# # Linked Issues — это связанные задачи в GitHub. Связь создаётся через упоминание номера другой Issue (#15) или добавление ссылки на неё в описании или комментарии.
#
# Какие метрики (например, время выполнения) можно отслеживать с помощью Issues?
#
# # Через Issues можно отслеживать время выполнения задач, количество открытых/закрытых задач, скорость работы команды, активность обсуждений, нагрузку исполнителей и распределение задач по категориям.
#
# Какие best practices рекомендуются при работе с Issues в команде?
#
# # Использовать понятные описания, Labels, Assignees, шаблоны, Milestones и Projects; избегать дубликатов, поддерживать актуальность задач, связывать Issues с Pull Requests и вести обсуждение в комментариях.
